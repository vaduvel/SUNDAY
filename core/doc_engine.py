import os
from datetime import datetime
from typing import List, Dict

class DocEngine:
    """
    J.A.R.V.I.S. Document Factory (Supreme Grade)
    Generates professional reports, business plans, and audit packs.
    """

    def __init__(self, output_dir: str):
        self.output_dir = output_dir
        os.makedirs(self.output_dir, exist_ok=True)

    def generate_pdf_report(self, title: str, content: str, author: str = "J.A.R.V.I.S.") -> str:
        """
        Creates a markdown-based report that can be exported as PDF.
        """
        # We generate Markdown which is then converted by the backend to PDF
        filename = f"{title.replace(' ', '_')}_{datetime.now().strftime('%Y%m%d')}.md"
        filepath = os.path.join(self.output_dir, filename)

        markdown_content = f"# {title}\n\n"
        markdown_content += f"**Date:** {datetime.now().strftime('%Y-%m-%d %H:%M')}\n"
        markdown_content += f"**Author:** {author}\n\n"
        markdown_content += "---\n\n"
        markdown_content += content
        markdown_content += "\n\n---\n*CONFIDENTIAL // GENERATED BY AEON SUPREME NUCLEUS*"

        with open(filepath, "w") as f:
            f.write(markdown_content)
        
        print(f"📄 [DOCGEN] Report generated: {filepath}")
        return filepath

    def create_business_plan(self, concept: str) -> str:
        """
        Generates a 2026-standard business plan for a new concept.
        """
        content = f"## Executive Summary for {concept}\n\n"
        content += "1. Market Analysis // 2. Resource Allocation // 3. Profit Projection..."
        return self.generate_pdf_report(f"Business Plan - {concept}", content)

